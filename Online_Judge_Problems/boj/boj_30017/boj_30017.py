import sys

# 패티 개수 A, 치즈 개수 B 입력
a, b = map(int, sys.stdin.readline().split())

# 패티는 치즈보다 최대 1개 더 많을 수 있음
# 만약 치즈가 아주 많아도 패티가 a개뿐이면, 치즈는 a-1개까지만 쓸 수 있음
# 만약 패티가 아주 많아도 치즈가 b개뿐이면, 패티는 b+1개까지만 쓸 수 있음

if a > b:
    # 패티가 더 많은 경우: 치즈를 다 쓰고 패티를 하나 더 얹음
    print(2 * b + 1)
else:
    # 치즈가 더 많거나 같은 경우: 패티를 다 쓰고 치즈를 하나 적게 넣음
    print(2 * a - 1)
